package com.tastecoordi.web.vo;

public class ClothesCoordi {
	private String code;
	private String clothesCode;
	private String CoordiesCode;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getClothesCode() {
		return clothesCode;
	}
	public void setClothesCode(String clothCode) {
		this.clothesCode = clothCode;
	}
	public String getCoordiesCode() {
		return CoordiesCode;
	}
	public void setCoordiesCode(String coordiCode) {
		this.CoordiesCode = coordiCode;
	}

}
